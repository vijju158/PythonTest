#Numeric, String & Tuples are immutable Data Types
#Numeric - Integer, Float, Complex Number
#Integer
a = 5
print(a)
print("Type of a: ", type(a))
#Float
b = 5.0
print(b)
print("Type of b: ", type(b))
#Complex
c = 2 + 4j
print(c)
print("Type of c: ", type(c))

#String
String = "Im a StringGeek"
print(String)
print("Type of String: ", type(String))

Tuple1 = (0, 1, 2, 3)
Tuple2 = ('python', 'geek')
Tuple3=(1,2,2,3.15,'edureka',2+1j)
print(Tuple3)
print(Tuple3[4])
print("Type of Tuple: ", type(Tuple1))